function renderDepositsList(data) {
    const container = document.getElementById('deposits-container');
    container.innerHTML = ''; // очистим на всякий случай

    Object.values(data).forEach(deposit => {
        const item = document.createElement('a');
        item.className = 'list-group-item list-group-item-action deposit-item';
        item.dataset.id = deposit.id;

        item.innerHTML = `
                <div class="d-flex w-100 justify-content-between">
                    <h5 class="mb-1">${deposit.location}</h5>
                    <button class="btn-primary btn-lg btn" asp-controller="License" asp-action="License" asp->Подробнее</button>
                </div>
                <p class="mb-1">Запасы: ${deposit.reserves || '—'}</p>
                <small>Лицензия ${deposit.license} до ${deposit.endDate}</small>
                
            `;

        container.appendChild(item);
    });
}

